// Helper functions for mock data
export function getSuppliersByCategory(category: string): Supplier[] {
  return mockSuppliers.filter(supplier =>
    supplier.categories.some(cat => cat.toLowerCase().includes(category.toLowerCase()))
  );
}

export function getSuppliersByLocation(location: string): Supplier[] {
  return mockSuppliers.filter(supplier =>
    supplier.location.toLowerCase().includes(location.toLowerCase())
  );
}

export function getTopRatedSuppliers(limit: number = 10): Supplier[] {
  return mockSuppliers.sort((a, b) => b.rating - a.rating).slice(0, limit);
}

export function getVerifiedSuppliers(): Supplier[] {
  return mockSuppliers.filter(
    supplier =>
      supplier.verificationLevel === 'verified' || supplier.verificationLevel === 'premium'
  );
}
